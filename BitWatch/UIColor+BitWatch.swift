import Foundation
import UIKit

extension UIColor {
    func intervalGreen() -> UIColor {return UIColor(red:(124.0/255.0), green:(208.0/255.0), blue:(126.0/255.0), alpha: 1.0)}
    func workPurple() -> UIColor {return UIColor(red: (178.0/255.0), green: (124.0/255.0), blue: (209.0/255.0), alpha: 1.0)}
    func restRed() -> UIColor {return UIColor(red: (208.0/255.0), green: (93.0/255.0), blue: (93.0/255.0), alpha: 1.0)}
}

